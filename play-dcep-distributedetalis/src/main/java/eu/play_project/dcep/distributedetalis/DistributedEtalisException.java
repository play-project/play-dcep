package eu.play_project.dcep.distributedetalis;

public class DistributedEtalisException extends Exception {

	private static final long serialVersionUID = -4375925562436280537L;

	public DistributedEtalisException() {
		super();
	}

	public DistributedEtalisException(String message, Throwable cause) {
		super(message, cause);
	}

	public DistributedEtalisException(String message) {
		super(message);
	}

	public DistributedEtalisException(Throwable cause) {
		super(cause);
	}

}
