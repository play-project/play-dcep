package eu.play_project.dcep.distributedetalis.api;

public class EcConnectionmanagerException extends Exception {

	private static final long serialVersionUID = 9068296223733710934L;

	public EcConnectionmanagerException() {
		super();
	}

	public EcConnectionmanagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public EcConnectionmanagerException(String message) {
		super(message);
	}

	public EcConnectionmanagerException(Throwable cause) {
		super(cause);
	}
}
